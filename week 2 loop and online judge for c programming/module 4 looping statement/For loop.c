
#include<stdio.h>
int main()
{
    int i;
    int count=0;

    for( i=1; i<=10;i++)  // increament system
    {
        printf("%d hello world\n",i);

        count++;
    }
    printf("\nTotal %d times\n",count); //total count korte hoy eivabe
    return 0;
}
