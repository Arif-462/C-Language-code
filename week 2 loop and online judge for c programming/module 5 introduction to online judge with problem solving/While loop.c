#include<stdio.h> // increament system
int main()
{
    int i=0;
    int count=0;

    while(i<10)
    {
        printf("%d hello world\n",i);
        i++;
        count++;
    }
    printf("\nTotal %d times\n",count); //total count korte hoy eivabe
    return 0;
}

